void draw_digit(int d, int x, int y, int scale, byte color){
	for(int dy = 0; dy < DIGIT_HEIGHT; dy++){
		for(int dx = 0; dx < DIGIT_WIDTH; dx++){
			if(RASTER_DIGITS[d][dy][dx]){
				for(int i = 0; i < scale; i++){
					for(int j = 0; j < scale; j++){
						vga_plot(x + dx * scale + j, y + dy * scale + i, color);
					}
				}
			}
		}
	}
}